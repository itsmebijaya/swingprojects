    public void getFlightId() throws SQLException {

        /* ----- Automatic Incrementing the Flight Id ------ */
        Connection cn = DBhelper.getConnection();
        Statement st;

        st = cn.createStatement();
        ResultSet rs = st.executeQuery("select MAX(flightid) from flight");
      
        if (rs.next()) {
            if (rs.getString("MAX(flightid)") == null) {
                txtflightid.setText("FO001");

            } else {
                long id = Long.parseLong(rs.getString("MAX(flightid)").substring(2, rs.getString("MAX(flightid)").length()));
                id++;
                txtflightid.setText("FO" + String.format("%03d", id));

            }

        }
    }

