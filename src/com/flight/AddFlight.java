    private void addFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFlightActionPerformed

        /* ---- Action Performed for adding the flight ----- */
        String flightid = txtflightid.getText();
        String flightname = txtflightname.getText();
        String source = txtsource.getSelectedItem().toString().trim();
        String departure = txtdeparture.getSelectedItem().toString().trim();

        DateFormat df = new SimpleDateFormat("yyyy-MM--dd");
        String date = df.format(txtdate.getDate());

        String departuretime = txtdeparturetime.getText();
        String arrivaltime = txtarrivaltime.getText();
        String flightcharge = txtflightcharge.getText();

        /* ------ Database Connection to insert the flight data into database ------ */
        Connection cn = DBhelper.getConnection();
        try {
            PreparedStatement pstmt = cn.prepareStatement("insert into flight values (?,?,?,?,?,?,?,?)");
            pstmt.setString(1, flightid);
            pstmt.setString(2, flightname);
            pstmt.setString(3, source);
            pstmt.setString(4, departure);
            pstmt.setString(5, date);
            pstmt.setString(6, departuretime);
            pstmt.setString(7, arrivaltime);
            pstmt.setString(8, flightcharge);

            int result = pstmt.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Flight Added");
            } else {
                JOptionPane.showMessageDialog(this, "Flight Not Added");
            }
        } catch (SQLException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addFlightActionPerformed

