    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String source = txtsource.getSelectedItem().toString().trim();
        String departure = txtdeparture.getSelectedItem().toString().trim();

        /* ------ Database Connection ------ */
        Connection cn = DBhelper.getConnection();
        PreparedStatement pstmt;
        try {
            pstmt = cn.prepareStatement("select * from flight where source=? and departure=?");
            pstmt.setString(1, source);
            pstmt.setString(2, departure);

            ResultSet rs = pstmt.executeQuery();

            ResultSetMetaData rsm = rs.getMetaData();
            /* To display the data in JTable */
            int c;
            c = rsm.getColumnCount();

            DefaultTableModel Df = (DefaultTableModel) jTable1.getModel();
            Df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {

                    v2.add(rs.getString("flightid"));
                    v2.add(rs.getString("flightname"));
                    v2.add(rs.getString("source"));
                    v2.add(rs.getString("departure"));
                    v2.add(rs.getString("date"));
                    v2.add(rs.getString("departuretime"));
                    v2.add(rs.getString("arrivaltime"));
                    v2.add(rs.getString("flightcharge"));
                }

                Df.addRow(v2);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            Logger.getLogger(Tickets.class.getName()).log(Level.SEVERE, null, ex);
        }

        System.out.println("Table Data loaded");

    }//GEN-LAST:event_jButton3ActionPerformed

