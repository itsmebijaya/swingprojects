    public final void autoid() throws SQLException {

        Connection cn = DBhelper.getConnection();
        Statement st = cn.createStatement();
        ResultSet rs = st.executeQuery("select MAX(ticketid) from tickets");

        if (rs.next()) {
            if (rs.getString("MAX(ticketid)") == null) {
                txtno.setText("TI001");

            } else {
                long id = Long.parseLong(rs.getString("MAX(id)").substring(2, rs.getString("MAX(ticketid)").length()));
                id++;
                txtno.setText("TI" + String.format("%03d", id));

            }
        }

    }

